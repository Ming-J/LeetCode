#include <iostream>
using namespace std;

int main(){
  int a1, a2, a3, a4;
  cin>>a1>>a2>>a3>>a4;
  string s;
  cin>>s;
  unsigned long long int sum = 0;
  for(const char& c: s){
    if(c=='1') sum += a1;
    else if(c=='2') sum += a2;
    else if(c=='3') sum += a3;
    else sum += a4;
  }
  cout<<sum<<endl;
  return 0;
}
